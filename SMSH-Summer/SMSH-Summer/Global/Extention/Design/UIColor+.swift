import UIKit

extension UIColor {
    
    @nonobjc class var primaryColor: UIColor {
        return UIColor(red: 254.0 / 255.0, green: 175.0 / 255.0, blue: 79.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var secondaryColor: UIColor {
        return UIColor(red: 31.0 / 255.0, green: 26.0 / 255.0, blue: 58.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var Black: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var Gray: UIColor {
        return UIColor(red: 193.0 / 255.0, green: 193.0 / 255.0, blue: 193.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var DarkGray: UIColor {
        return UIColor(red: 65.0 / 255.0, green: 65.0 / 255.0, blue: 65.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var LightGray: UIColor {
        return UIColor(red: 248.0 / 255.0, green: 248.0 / 255.0, blue: 248.0 / 255.0, alpha: 1.0)
    }
}
